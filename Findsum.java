//Find the sum of 1 to 10 natura
public class Findsum {

	public static void main(String[] args) {
		int i,sum=0;
		
		i=1;
		while(i<=10) {        //Condition
			                //System.out.println("sum="+sum+"+"+i);
			sum=sum+i;      //sum=0+1=1 sum=1+2=3 sum3+3=6
			i=i+1;
			
			
		}
		
         System.out.println("sum="+sum);
	}

}
